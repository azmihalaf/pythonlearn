# Operator dalam bentuk methods

## merubah case dari string

# merubha semua ke upper case

salam = "bro!"
print("Normal = " + salam)
salam = salam.upper()
print("Normal = " + salam)

# merubah semua ke lower case

alay = "aKu KeCe AbiEeEeZzZzzzZ"
print("Normal = " + alay)
alay = alay.lower()
print("Normal = " + alay)

